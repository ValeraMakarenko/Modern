CREATE TABLE STATUS (
	id_status BIGINT GENERATED BY DEFAULT AS IDENTITY,
	name_status VARCHAR (255) NOT NULL
);

CREATE TABLE VOTING (
  id_voting BIGINT GENERATED BY DEFAULT AS IDENTITY,
  theme VARCHAR (255) NOT NULL,
  link VARCHAR (255) NOT NULL,
  fk_id_status BIGINT NOT NULL,
  CONSTRAINT voting_status FOREIGN KEY (fk_id_status) REFERENCES STATUS (id_status)
  ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE QUESTION (
  id_question BIGINT GENERATED BY DEFAULT AS IDENTITY,
  text_question VARCHAR (255) NOT NULL,
  fk_id_voting BIGINT ,
  CONSTRAINT question_voting FOREIGN KEY (fk_id_voting) REFERENCES VOTING (id_voting)
  ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE ANSWER (
  id_answer BIGINT GENERATED BY DEFAULT AS IDENTITY,
  text_answer VARCHAR (255) NOT NULL,
  count BIGINT,
  fk_id_question BIGINT ,
  CONSTRAINT answer_question FOREIGN KEY (fk_id_question) REFERENCES QUESTION (id_question)
  ON UPDATE CASCADE ON DELETE CASCADE
);
